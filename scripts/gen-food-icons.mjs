/**
 * Generates `src/lib/app/food-icons.ts` from `scripts/food-icons.data.json`.
 *
 * The data file is a set of flat per-dish icons (idli, dosa, vada, …) authored
 * via the design-food-icons workflow (designer + adversarial critic). Each dish
 * is a list of primitive shapes in a 0..24 box; the FoodIcon component renders
 * them deterministically. No IP concern — these are generic dish illustrations.
 *
 * Run:  node scripts/gen-food-icons.mjs   (then `pnpm format` tidies output)
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DATA = join(__dirname, 'food-icons.data.json');
const OUT = join(__dirname, '..', 'src', 'lib', 'app', 'food-icons.ts');

const dishes = JSON.parse(readFileSync(DATA, 'utf8'));

// Flat, appetizing palette. Tokens referenced by the shape data; anything not
// here renders as `brown` so a typo never produces an invisible shape.
const PALETTE = {
  plate: '#E8EDF1',
  plateline: '#BAC4CE',
  steam: '#B2BDC8',
  white: '#FCFBF7',
  cream: '#F2E6C4',
  golden: '#E8A33D',
  deepgold: '#C8791A',
  amber: '#F4CD7A',
  brown: '#97592A',
  darkbrown: '#5E3917',
  ghee: '#F4C543',
  coconut: '#F3F0E6',
  green: '#67B25C',
  darkgreen: '#3C7A37',
  mustard: '#C28A1B',
  peppercorn: '#2C2824',
  tomato: '#E0552C',
  red: '#D13A28',
  maroon: '#8B2A21',
  peanut: '#D8B886'
};

// Keywords that must NOT claim a dish (collide with a more specific dish).
const DENY = new Set(['paratha|parotta', 'chutney|coconut']);

const keyValid = new Set([
  'k',
  'cx',
  'cy',
  'rx',
  'ry',
  'r',
  'x',
  'y',
  'w',
  'h',
  'round',
  'points',
  'x1',
  'y1',
  'x2',
  'y2',
  'fill',
  'stroke',
  'sw'
]);

const iconEntries = [];
const keywordPairs = [];
const claimed = new Set();

for (const d of dishes) {
  // Sanitize shapes: keep only known fields, coerce nothing else.
  const shapes = d.shapes.map((s) => {
    const o = {};
    for (const k of Object.keys(s)) if (keyValid.has(k)) o[k] = s[k];
    return o;
  });
  iconEntries.push([d.dish, { label: d.label, shapes }]);
  for (const kw of d.keywords) {
    const norm = kw.toLowerCase().trim();
    if (DENY.has(`${d.dish}|${norm}`)) continue;
    if (claimed.has(norm)) continue; // first (earlier dish) wins; we order data deliberately
    claimed.add(norm);
    keywordPairs.push([norm, d.dish]);
  }
}

// Longest keyword first so "masala dosa" beats "dosa", "idiyappam" beats "appam".
keywordPairs.sort((a, b) => b[0].length - a[0].length);

const body = `/**
 * AUTO-GENERATED by scripts/gen-food-icons.mjs from scripts/food-icons.data.json.
 * DO NOT EDIT BY HAND — edit the data file and re-run the generator.
 *
 * Distinct flat icon per dish (idli != dosa != vada != …), designed via the
 * design-food-icons workflow. Generic dish art, no trademarks involved.
 */

/** A single drawing primitive in a 0..24 viewBox. Fields used depend on \`k\`. */
export interface FoodShape {
  k: 'ellipse' | 'circle' | 'rect' | 'tri' | 'poly' | 'line';
  cx?: number;
  cy?: number;
  rx?: number;
  ry?: number;
  r?: number;
  x?: number;
  y?: number;
  w?: number;
  h?: number;
  round?: number;
  points?: number[][];
  x1?: number;
  y1?: number;
  x2?: number;
  y2?: number;
  /** Palette token (see FOOD_PALETTE) or "none". */
  fill?: string;
  /** Palette token or "none". */
  stroke?: string;
  sw?: number;
}

export interface FoodIconDef {
  label: string;
  shapes: FoodShape[];
}

export type FoodKey = ${iconEntries.map(([k]) => `'${k}'`).join(' | ')};

/** Palette token -> hex. Unknown tokens fall back to brown at render time. */
export const FOOD_PALETTE: Record<string, string> = ${JSON.stringify(PALETTE, null, 2)};

export const FOOD_ICONS: Record<FoodKey, FoodIconDef> = {
${iconEntries.map(([k, v]) => `  ${k}: ${JSON.stringify(v)}`).join(',\n')}
};

/** [keyword, foodKey] pairs, longest keyword first (most specific wins). */
export const FOOD_KEYWORDS: ReadonlyArray<readonly [string, FoodKey]> = [
${keywordPairs.map(([kw, k]) => `  ['${kw}', '${k}']`).join(',\n')}
];
`;

writeFileSync(OUT, body, 'utf8');
console.log(`Wrote ${OUT}`);
console.log(`  ${iconEntries.length} food icons, ${keywordPairs.length} keywords`);
